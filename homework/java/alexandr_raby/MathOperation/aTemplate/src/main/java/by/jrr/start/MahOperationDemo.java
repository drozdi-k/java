package by.jrr.start;

public class MahOperationDemo {

    public static void main(String[] args){
        System.out.println("Welcome to the 'Math Operations'!");

        int a = 90;
        int b = 40;

        System.out.println("1) Addition '+':");
        System.out.print("a = ");
        System.out.print(a);
        System.out.print(", ");
        System.out.print("b = ");
        System.out.print(b);

        int sum = a + b;

        System.out.println();
        System.out.print("a + b = ");
        System.out.print(a);
        System.out.print(" + ");
        System.out.print(b);
        System.out.print(" = ");
        System.out.print(sum);


        double c = 10.18;
        double d = 2.14;

        System.out.println();
        System.out.println();
        System.out.println("2) Substraction '-':");
        System.out.print("c = " + c +", d = " + d);

        double dif = c - d;

        System.out.println();
        System.out.print("c - d = " + c + " - " + d + " = " + dif);

        int x = 15;
        int y = 4;

        System.out.println();
        System.out.println();
        System.out.println("3) Remainder '%':");
        System.out.println("x = " + x + ", " + "y = " + y);

        int rem = x % y;

        System.out.println("x % y = " + x + " % " + y + " = " + rem);










    }
}
