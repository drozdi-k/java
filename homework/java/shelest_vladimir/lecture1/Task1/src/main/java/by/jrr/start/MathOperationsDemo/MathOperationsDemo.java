package MathOperationsDemo;

public class MathOperationsDemo {
    public static int addition(int first, int second) {
        return first + second;
    }

    public static int subtraction(int subtrahend, int subtract) {
        return subtrahend - subtract;
    }

    public static int multiplication(int first, int second) {
        return first * second;
    }

    public static double division(int devidend, int devider) {
        return (double)devidend / (double)devider;
    }
}
